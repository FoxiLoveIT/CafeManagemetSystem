    public void validateField() {
        String customerName = txtCusName.getText();
        String customerMobileNumber = txtCusMobNum.getText();
        String customerCard = txtCard.getText();
        if (!customerName.equals("") && customerMobileNumber.matches(mobileNumberPattern) && customerMobileNumber.length() == 10 && customerCard.matches(cardPattern) && customerCard.length() == 12 && grandTotal > 0) {
            btnGenerateBill.setEnabled(true);
        } else {
            btnGenerateBill.setEnabled(false);
        }
    }


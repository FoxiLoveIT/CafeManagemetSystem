    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        // TODO add your handling code here:
        String customerName = txtCusName.getText();
        String customeMobileNumber = txtCusMobNum.getText();
        String customerCard = txtCard.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String todaydate = dFormat.format(date);
        vatCount = grandTotal /120 * 17;
        String total = String.valueOf(grandTotal);
        String totalVat = String.format("%.2f", vatCount);
        String createdBy = userCard;
        Bill bill = new Bill();
        bill.setId(billid);
        bill.setName(customerName);
        bill.setMobileNumber(customeMobileNumber);
        bill.setCard(customerCard);
        bill.setDate(todaydate);
        bill.setTotal(total);
        bill.setTotalVat(totalVat);
        bill.setCreatedBy(createdBy);
        BillDao.save(bill);

        // создание чека
        String path = "C:/Bill/";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path + "" + billid + ".pdf"));
            doc.open();
            Paragraph cafeName = new Paragraph("                                                                         CoffeFox \n");
            doc.add(cafeName);
            Paragraph starLine = new Paragraph("****************************************************************************************************************");
            doc.add(starLine);
            Paragraph address = new Paragraph("\n                                      Street: 22 HaBarzel City: Tel Aviv \n");
            doc.add(address);
            doc.add(starLine);
            Paragraph paragraph3 = new Paragraph("\tBill ID:" + billid + "\nCustomer Name " + customerName);
            doc.add(paragraph3);
            doc.add(starLine);
            PdfPTable tb1 = new PdfPTable(5);
            tb1.addCell("Name");
            tb1.addCell("Size");
            tb1.addCell("Price");
            tb1.addCell("Quantity");
            tb1.addCell("Total");
            for (int i = 0; i < jTable2.getRowCount(); i++) {
                String n = jTable2.getValueAt(i, 0).toString();
                String s = jTable2.getValueAt(i, 1).toString();
                String d = jTable2.getValueAt(i, 2).toString();
                String r = jTable2.getValueAt(i, 3).toString();
                String q = jTable2.getValueAt(i, 4).toString();
                tb1.addCell(n);
                tb1.addCell(s);
                tb1.addCell(d);
                tb1.addCell(r);
                tb1.addCell(q);
            }
            doc.add(tb1);
            Paragraph totalprice1 = new Paragraph("                                                                                                           Total Paid: " + total);
            doc.add(totalprice1);
            Paragraph totalVat1 = new Paragraph("                                                                                                           including tax 17%: " + totalVat);
            doc.add(totalVat1);
            doc.add(starLine);
            Paragraph thanksMsg = new Paragraph("                                                                          Thank you, Prease come Again.");
            doc.add(thanksMsg);

            OpenPdf.openById(String.valueOf(billid));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        doc.close();
        setVisible(false);
        new PlaceOrder(createdBy).setVisible(true);
    }//GEN-LAST:event_btnGenerateBillActionPerformed

